#include "printf.h"

int	ft_putnbr_uns(unsigned int n)
{
	int	i;

	i = 0;
	if (n > 9)
	{
		ft_putnbr_uns(n / 10);
		ft_putnbr_uns(n % 10);
	}
	if (n <= 9)
	{
		ft_putchar(n + '0');
		i++;
	}
	return (i);
}
